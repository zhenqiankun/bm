package mmn;

public class person {
    public String name;
    public String sex;
    public String indress;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getIndress() {
        return indress;
    }

    public void setIndress(String indress) {
        this.indress = indress;
    }
}
